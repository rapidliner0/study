user = "Tuna McFish"
# Nothing printed on sys.stdout

user[0]
# 'T'

user[5]
# 'M'

user[-1]
# 'h'

user[-3]
# 'i'

user[2:7]
# 'na Mc'

user[:7]
# 'Tuna Mc'

user[2:]
# 'na McFish'

user[:]
# 'Tuna McFish'

print("dsadasd")
# dsadasd

